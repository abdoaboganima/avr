#ifndef _STD_TYPES_H_
#define _STD_TYPES_H_

/* 1-Byte Data Type*/
typedef unsigned char u8;
typedef signed char s8;

/* 2-Byte Data Type */
typedef unsigned short u16;
typedef signed short s16; 

/* 4-Byte Data Type*/
typedef unsigned int u32;
typedef signed int s32;

/* Floating Data Type*/
typedef float f32;
typedef double f64;

#define NULL 0

#endif /* _STD_TYPES_H_ */
